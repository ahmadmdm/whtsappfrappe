export function useLocale() {
	const lang = (window.frappe?.boot?.lang || document.documentElement.lang || "en").toLowerCase()
	const locale = lang.startsWith("ar") ? "ar" : "en"
	const dir = computed(() => (locale === "ar" ? "rtl" : "ltr"))
	const dictionary = messages[locale]

	function t(key, ...args) {
		const template = dictionary[key] || messages.en[key] || key
		return args.reduce((text, value, index) => text.replace(`{${index}}`, value), template)
	}

	return { locale, dir, isArabic: locale === "ar", t }
}